   private void initTable() {
    tableModel = new DefaultTableModel(
        new Object[]{"ID Nasabah", "Nama", "Jenis Kelamin", "No Hp", "Alamat", "Umur"},
        0
    );
    tblNasabah.setModel(tableModel);
}
    private void loadNasabah() {
    tableModel.setRowCount(0);

    for (Nasabah n : nasabahService.getAllNasabah()) {
        tableModel.addRow(new Object[]{
            n.getIdNasabah(),
            n.getNama(),
            n.getJenisKelamin(),
            n.getNomorHp(),
            n.getAlamat(),
            n.getUmur()
        });
    }
}
