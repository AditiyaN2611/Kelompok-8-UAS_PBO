    private void btnPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPilihActionPerformed
        int row = tblNasabah.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(
                this,
                "Silakan pilih nasabah terlebih dahulu",
                "Peringatan",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        // Ambil data dari tabel
        idNasabahTerpilih = tblNasabah.getValueAt(row, 0).toString();
        String namaNasabah = tblNasabah.getValueAt(row, 1).toString();

        // Set ke form
        txtNama.setText(namaNasabah);

        JOptionPane.showMessageDialog(
            this,
            "Nasabah berhasil dipilih",
            "Info",
            JOptionPane.INFORMATION_MESSAGE
        );
    }//GEN-LAST:event_btnPilihActionPerformed

